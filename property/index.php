    	<h2><a name="cuisine">Cuisine</a></h2>
    	<p>
		This area of is one of the best places in Andalusia for tapas. A myriad of bars offer you a variety of tapas to go along with your beer, or with the full-bodied wines produced 
		in this region. In the neighbourhoods of Pescadería and El Alquián, both in Almería and in Cape Gata, fish tapas are very popular, while in the city centre it is more common to 
		find hot tapas like different types of casseroles, patatas pobres or pauper's potatoes (sliced and fried with onions). In the province of Almería there is a varied spoon cuisine, 
		which may seem more typical of Spanish inland areas than of a coastal city to the visitor. Soups and stews are typical, like the Almería-style soup (seafood soup), Almería-style 
		garlic soup, fritada from Suflí (which is a kind of tomato sauce), Moorish soup, black soup and pimentón or red soup (a kind of fish casserole). Other great dishes are the fennel
		and wheat stew called olla de trigo and the renowned gurullos, a country dish that consists of small flour balls, fried in lard and flavoured with garlic, spicy sausage, bacon, 
		game meat, etc.
		</p>
