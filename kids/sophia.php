<strong>Monday 27th August 2012</strong>
</p>
Observation:
<p>
Jessica and Loki were standing underneath the cubby house with Sophia. Jessica tells Sophia “Do you want ice cream?” Sophia nods her head and pretends to 
hand over something to Jessica. Jessica then pretends to make something with her hands and then hands their imaginary ice cream to Sophia. Sophia then puts 
her hand in front of her mouth and begins to chew. The teacher asks Sophia “Is the ice cream yummy?” Sophia replies “Yes!”. Jessica then asks Sophia “Do you 
want more?” and Sophia nods her head and says “More ice cream.” Sophia then walks to the other side and pretends to be making ice cream. The teacher tells her
 “Here is my money Sophia. Can I have some vanilla ice cream?” Sophia places her hand near a small hole by the side of the pillar and then hands over an 
 imaginary ice cream to the teacher.
 </p>
