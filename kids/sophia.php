				<h4>Day Care</h4>
				<p>
<strong>Monday 20th August 2012</strong>
</p>
Observation:
<p>

Patty had set up a little “create your own face” corner for the toddlers and Sophia was standing beside it, looking at the set up. The teacher asked Sophia 
if she wanted to try building her own face. Sophia was looking at the different pieces, as if trying to figure out how they all fit in. The teacher got an 
eye and stuck it on the face. After seeing this, Sophia then took another eye and stuck it on the face. The teacher then asked Sophia if she wanted to put a 
moustache on the face. “Look, Sophia, a moustache!” The teacher said, as she placed the moustache above her lip and began to laugh “bwa-ha-ha”. Sophia smiled.
 The teaher handed the moustache to Sophia and said “Do you want to try the moustache?” Sophia took the moustache and placed it above her lip and did a fake
 manly laugh “bwa-ha-ha!”
 </p>
<a href="http://www.flickr.com/photos/catanddog/7990621389/" title="sophia.face by Cat and Dog, on Flickr"><img src="http://farm9.staticflickr.com/8442/7990621389_79e04ac2d6.jpg" width="300" height="224" alt="sophia.face"></a><br>
(Sophia choosing a nice addition to the face she’s created)<br>
 <a href="http://www.flickr.com/photos/catanddog/7990631024/" title="sophia.moustache by Cat and Dog, on Flickr"><img src="http://farm9.staticflickr.com/8320/7990631024_30a5cef3da.jpg" width="300" height="224" alt="sophia.moustache"></a>
<br> (“Bwa-ha-ha!” Sophia with a moustache.)
 
<p>
Interpretation:
</p>
After having joined the toddler room fairly recently, Sophia is quite new to her new environment. Even the toys are new! Sophia appeared to be interested in 
the face puzzle but she seemed to be a bit hesitant as to how to approach it. When the teacher showed her how she could mix and match the face pieces, Sophia 
then jumped right in and began playing on her own. Although things might be new to Sophia, she appears to be open to the novelty of it all as seen by her 
willingness to try out the new learning activity she hasn’t encountered before. This is a good example of how Sophia is fulfilling EYLF outcome 3.1 which 
states that children should become strong in their social and emotional wellbeing.
</p>
<p>
Follow up:
</p>
<p>
It was great to see Sophia’s confidence shine through as she goes through this significant change in her life in daycare. Now that she is in the 
toddler room, Sophia has new friends to make and new and exciting activities to learn! It would be great to encourage Sophia to engage in 
the learning experiences that are set up in the toddler room and praise her for a job well done after she finished with an activity. This is 
one way for Sophia to feel confident as she adjusts to her new surroundings.
</p>
