 <p>
 Interpretation:
<p>
Sophia, Jessica, and Loki’s play experience was very interesting to watch. In their little ice cream store scenario, the three children seemed to play 
different roles. Jessica and Loki were the ice cream vendor and Sophia was the customer. It was amazing to see the children act out what they perceived 
to be their role in their little imaginative play. Their actions included Sophia handing some money to Jessica who then prepared ice cream to give back to
 Sophia. Sophia would then proceed to “eat” her ice cream. Sophia also had a turn being the ice cream vendor and she acted out her part so well!
</p>
<p>
It was great to see Sophia fit in so well with the other toddlers after transferring to the toddler room fairly recently. Not only play so well with 
her friends, she was also exploring a different type of play as she explored a different identity in their dramatic play (EYLF outcome 1.3- “children 
develop knowledgeable and confident self-identities).
</p>
<p>Follow up:
</p>
<p>
Imaginary play is a great way for the children to explore their creativity. As an extension to this learning experience, the teacher can set up a
 home corner for Sophia wherein she can do other forms of imaginary play like cooking and baking with the help of some props.
 </p>

<strong>Monday 3rd Sept 2012:</strong><br>				
				Observation:
<p>
Sophia came over to visit the babies this morning and she was quick to walk over to the home corner where the teacher was playing with Claudia. 
Sophia got a teacup and held it up as Claudia pretended to pour some imaginary tea inside. The teacher told Sophia “Be careful Sophia, it’s hot!” 
Sophia replied “It’s hot!. She then proceeded to blow into the cup and pretended to sip from it.
</p>
<a href="http://www.flickr.com/photos/catanddog/7990630818/" title="sophia.tea by Cat and Dog, on Flickr"><img src="http://farm9.staticflickr.com/8454/7990630818_8469e47439.jpg" width="300" height="224" alt="sophia.tea"></a><br>
 (Sophia wanted some tea.)<br>
<a href="http://www.flickr.com/photos/catanddog/7990621731/" title="sophia.drink.tea by Cat and Dog, on Flickr"><img src="http://farm9.staticflickr.com/8042/7990621731_2715b94560.jpg" width="300" height="224" alt="sophia.drink.tea"></a><br>
 (Drinking her “hot tea” as Claudia looks on)
<P>
Interpretation:
</p>
<p>
This spontaneous experience is a good extension to Sophia’s earlier “ice cream store” imaginary play with her friends from the toddler room. This time around, 
Sophia was able to use some props such as the teapots and teacups as part of her play. Sophia was also engaged in a conversation with the teacher and Claudia 
as she played in the home corner. When the teacher mentioned that the tea might be hot, Sophia then responded by pretending that their imaginary tea was 
indeed hot, blowing into it before drinking it.
</p>
<p>
Follow up:
</p>
<p>
As Sophia spends more time in the toddler area, she has observed how some of the older children play and interact with one another. As a result, 
Sophia’s own interests in the types of play she engages in may also change. A good extension to her imaginary play is to set up a dress-up area where 
Sophia can choose a costume she could wear. Dress-ups are always fun for toddlers. They are also great for promoting children’s imagination, creativity 
and even self-confidence.
</p>
