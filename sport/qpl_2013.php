		</p>
		<p>
Sunday 28 April: the Porkies go to QP28 for a 1pm game.  Most were in attendance, and fairly dusty to boot!
		</p>
		<p>
The game started out with a bang - for WOBs, who took the Ladies in Red by surprise with their strength on the ball and quick pressure!  The play remained in the opposition's half, but QP lacked our standard control and composure.
		</p>
		<p>
Finally we broke through the defence with a stellar cross from Brianne, a rebound, and excellent positioning by Jen resulting in Goal 1!  Wooohoooooo!
		</p>
		<p>
Unfortunately, it was the only goal of the half, and the ladies struggled through another 25 minutes of heat and bouncy play until the half-time whistle blew. 
	</p>
	<p>
