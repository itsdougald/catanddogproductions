		<h4><a name="match-13"></a> [ 2013 Aug]</h4>
		Semi-finals
		<p>
		Happy Monday (oxymoron?) Porkies!
</p>
<p>
Queens Park has had an exciting, at times surprising, and overall fantastic season, and at the end of it all we landed ourselves the number 2 spot on the table!  Dunbar sat in their throne at the top of the list, with Maroubra taking a surprising third, and Coogee grappling for the bottom spot in the finals (where they belong!).
</p>
<p>
As customary in our league, 1 plays 2 and 3 plays 4, with the losers of 1v2 game playing the winners of 3v4 game for a spot in the finals.  Confused?  
</p>
<p>
Anyway, it meant we were up against Dunbar on Sunday, and brought it to their home stadium on NCIE.  With a very full squad of 16, with poor injured Emma C and Gentle Gill both in attendance for support.  Welcome back to Lyndsay and to Mrs Sonja by the way!!
</p>
<p>
The Porkies started out STRONG, rising to every header and hard challenge.  On such a small field, the play was constantly threatening one team's goal but we got the edge over Dunbar, controlling most of the play.  
</p>
<p>
Finally we made good on a corner kick by Emma G, when Yee and Lyndsay got their heads (or something like that) on the expertly placed serve.  After a quick frenzy, the ball rolled out the Brianne, waiting patiently at the top of the box.  Everyone froze.... and Brianne lightly passed the ball through the throng and into the goal.  GOOOOOALLL!
</p>
<p>
Fifteen minutes later, Yee made one of many dangerous runs down the left, beating her defender and popping the ball right across the mouth of the goal, over the short/ horizontally unchallenged keeper's hands, and right into the path of Aly who tapped it it.  GOOOOALLLLLL!!!
</p>
<p>
We were thrilled!!  2-0 to Dunbar in the first half!!
</p>
<p>
At half time we found a shady spot and heard some words of wisdom from Dougal.  Long story short... don't get too comfortable!!!  Dunbar won't go down without a fight!
</p>
<p>
And they certainly didn't, coming into the second half with lots of aggression.  Dirty Deans was on fire in goal, playing at all edges of the box and cutting down on attacks before they ever really began.  The defence were the true heroes of the match - between Deans' confident positioning and the defence's fearlessness in every challenge, Dunbar had minimal shots on goal!!
</p>
<p>
Highlights of the second half include one of our team's least favourite players, the mouthy number 8, being sent off with a double yellow card, and a comment after the game from the ref (who had awarded Ronnie-size with a red earlier in the season) that Ronnie was the star player of the match!!!  The jury is out on if Ronnie slipped him a $20 before the match.... :)
</p>
<p>
When the final whistle blew, Social Secretary Emma C was on hand with a bottle of bubbles on ice (THAT is confidence!) and classy plastic cups for all, and team pictures were in order.
</p>
<p>
As the winner of this game, we get a weekend off and then straight into the Finals!!!!  Coogee and Dunbar will face off this weekend to determine who our opposition will be, so stay tuned!
</p>
xx<br>
Aly

  <h4><a name="match-08">Queens Park Lions 2 (1) v (1) 2 Waverley Old Boys</a> [ 2013 July]</h4>
		
		Hi Porks!
 <p>
Now you may have noticed a lull in these masterful wraps... we have indeed been playing, and I have been slacking!  As beloved veteran Nic Belton recently said 'business must be booming!'
 </p>
So.  Here is the skinny on what us porkers have been up to:
 <p>
Sunday July 14, QP vs UNSW
 </p>
 <p>
We rolled up the very professional looking astroturf at David Philips and taught UNSW what was what!!
Emma G started us off with a goal, followed by 2 from Ronnie, and another from Emma G just before half time. Double-stuffed Emma G and Ronnie Oreo!
Emma G started out the second half as she started, and then Emma C got in on the action with three in a row!  Yee had an expert header off the crossbar followed in by Emma C.  Emma C finished with a bang with a header into the goal off of a cross.  UNSW - don't mess with Irish gals!!!
We win, 7-0.
 </p>
 <p>
Sunday July 21, QP vs Maroubra
 </p>
 <p>
Not the finest day at the office!!  We met Maroubra at their home ground of Coral Sea.   Queens Park just wasn't fighting for it, and was giving way to Maroubra time and again.  Despite this, we ran the game and had possession for 80% of the time.  We had dozens of shots, and just couldn't get any in!!  Mid-way through the first half, Maroubra countered an attack, getting behind our Midfield.  A huge cross from the right side hit the left mid, who belly-ed it into the back post.  Grrrrr!
Second half, a bit like the first.  A little bit louder and little but worse!!  (anybody get it?)
Again, we had many shots and no success.  We finally capitalised mid-way through the second half when Ronnie got one in off of a corner.  Wohhoooooo!
Ronnie then got a very controversial red card, sending her off for the next 3 games!!  Boo to the ref!!!
Not too much after this, and we left in sad spirits with a draw, 1-1.
 </p>
 <p>
Sunday 28 July, QP vs Dunbar
 </p>
 <p>
We met Dunbar where it all began for us this season, at adorable Queens Park 28 on a very sunny day.  With a lean squad and high hopes started fairly even against Dunbar.  Not too long in, we were showing definite signs of running the game.  Dunbar was playing their standard long-ball game and showing cracks with several calls against them and even a yellow card to on mouthy mid-fielder. 
Unfortunately for us, Dunbar did what they do best and made the most of the one attack they got through our defence and scored with a long shot from outside the box.  Boooo!
Not to be out-done, we answered back in the second half when Emma G sunk on into the net.  It. was. EPIC!
We continued to battle late into the second half when everyone's worst nightmare came true.  The mouthy Dunbar player got a high ball from way outside the box, near the sideline, juggled it, punted it backwards and the ball soared..... high into the back of our net.  A hit and a hope that paid off.  We were desolate.  But not to worry fair Porky Supporters, we'll have another chance to take Dunbar down later this year when we meet them in the finals!!
So to summarise, QP 1 - Dunbar 2.  :(
 </p>
 <p>
Saturday 3 August, QP vs Coogee
 </p>
 <p>
You may recall, we beat Coogee last time we played them so we had a reputation to upkeep!
 </p>
 <p>
Without a keeper in sight, brave EK and Cas volunteered to stand in goal for the Porkies, and will forever be remembered as legendary for their efforts.
 </p>
 <p>
Coogee didn't stand a chance!  We had them from the start, beating the team to almost every air ball and rushing to defend as a team when they won the ball.  In the first half, the opposition threatened us only a few times with 2 corners and one shot, masterfully batted aside by EK.  We answered back when Brianne, back from injury, received the ball from a quick throw from Jen.  B carried the ball to the corner for a quick cross over the top, to sink it into the back of the net. Boom!
Half time.  Caz puts on the keeper outfit of courage.
Coogee is getting scrappy and mouthy as always.  They got one very dangerous break away with defenders trailing behind, and brave Cas comes out the meet it, stopping the ball when the Coogee forward kicks it OUT OF her hands and lands ON TOP of Caz.  The ref, clearly unfamiliar with the laws of the game, does not call this and game resumes.  Defenders Vhari, Vicki, and Alana see the ball out as Caz pile drives the forward off of her head.  It was one of the most memorable sights of my Queens Park career.
Anyway, it was to no avail and soon Coogee got a corner.  We quickly countered when a porky chipped the ball over their defence off the corner to Maurisa, who was standing at the mid-field line on our half.  Maurisa turns it, already past the defense, as the cry off-sides.  Sorry Coogee, not when she is in her own half!  In a mad dash, Maurisa seals our win with a chip over the keeper and into the goal. 
We finish with a win, 2-0.  Woohoo!
 </p>
 <p>
Sunday 4 August, QP vs UNSW
 </p>
 <p>
Again, after beating UNSW so soundly last time, we had to prove we could do it again!  After winning against Coogee and with 2 more games in hand before the finals, we needed a win the cement our standings for Finals.
 </p>
 <p>
And we sure did!  Win.  By a lot.
 </p>
 <p>
Queens Park had the run of the game from start to finish, with poor Deans getting more and more bored out back.  (But we were still SO SO HAPPY to have her back!)
 </p>
 <p>
Emma C started the mojo going with her standard one touch turn on a dime/5 cent piece/ quid/ whatever currency you use.
 </p>
 <p>
Next up, Emma C passes to Ronnie who drives the ball into the top right corner, crosses, and then followed in by Jen!
 </p>
 <p>
The magic continues when EK carries the ball from mid-field into the left corner, to cross the right midfielder Tess and into the goal it goes.
 </p>
 <p>
Looking good for half-time kiddos, keep it up!
 </p>
 <p>
Brianne then amazed us all with two beaaautiful shots from outside the both, dead into the top right net. 
 </p>
 <p>
In between these goals, Emma G made a perfect corner kick, right to Ronnie-size who heads it into the left post and into the goal.
 </p>
 <p>
So it was another win for us 6-0!!
 </p>
 <p>
Afterward, we corralled at the Waverly Bowls Club for some good eats, orchestrated by the Social Secretary herself, Emma C.
 </p>
 <p>
Lots of excitement to follow when we play Waverly Old boys next week to exact our revenge and increase our points for the final standings of this years season!!!
 </p>
Cheers,<br>
Aly
		<h4><a name="match-07">Queens Park Lions 2 (1) v (1) 2 Waverley Old Boys</a> [ 2013 ]</h4>
		QP vs WOB
 
On Saturday, we clashed with the Old Boys at Centennial Park on a very hilly field.
 
Ronnie was off getting physio-cal, Tess is out with an ouchy hip flexer, and Brianne toughing it through with dodgy feet, so we were joined by the lovely Lady Leah from the O35s.
 
It took a while to shake the rust off and very unfortunately WOB got a goal in on us mid-way through the first half.  Not to be outdone, the Porques answered back when Aly dropped a tricksy corner right into the mouth of the goal for brave EK to knock out of the keepers' grasp with a tunning header.  Woot woot!!!
 
Very unfortunately the Old Boys answered back in the second half with a run up the centre and shot into the lower corner of our net.  Boo!
 
Emma G saved the day when she bent it like Beckham and scored off of a corner, the ball whipping into the far top 90. 
 
The Red Piglets had really picked up speed at this point but just couldn't land the ball in the net, despite a few great cracks on goal.
 
In the end, it wasn't the best result but it was a good result.  After playing the Old Boys, us Old Girls went off to rest our legs and prepare for our State Cup Game!
 

		<h4><a name="match-06">Queens Park Lions 0 (0) v (3) 4 Dunbar Rovers</a> [ 2013 May 12]</h4>
		Hello Porkies of the World!
<p>
Queens Park had another high-scoring escapade when we played Maroubra's second 2nd devision team (less confusing than it sounds) yesterday at their home pitch of Coral Sea.
</p>
<p>
Except for the lovely (and gentle) Gill, all players were in attendance with a mighty squad of 16!
</p>
<p>
Emma G started in true form with a run through several defenders at the top of the box, shot, and a goal in the first 5 minutes!!
</p>
<p>
Then Captain of the day EK scored a classic EK2 special, sprinting down the left side, shot across the frame, and goal 2 in the upper 90!!
</p>
<p>
For the next 30 minutes madness ensued.  There must have been something in the water because us Porkies were off our game!  Our usual finesse on and off the ball was at home having a sick day, and our attacks were stemmed time and time again by multiple off-sides calls. Thankfully we were able to keep most of the opposition's attacks at bay, and the ones that snuck through were quickly stifled by the mighty Dirty Deans!
</p>
<p>
Towards the end of the half a shot rebounded off the keeper's hands to the base line, snapped up by super-speedy Jen who tapped it back to Ronnie, who whipped the ball through three defenders, to be picked up by Lyndsey who then took a touch and easily placed the ball in the corner of the frame.  Teamwork at it's best!!!
</p>
<p>
Phew, 3-0 and we trot off the consult with Trigger/Couch/Dog/Dougal at half-time.
</p>
<p>
Dougal says: STOP BEING OFF-SIDES PLEASE! (And some other very productive words of wisdom).
</p>
<p>
Tess started the half with a huge answer back to our off-sides frustrations, when she received the ball in the centre, dribbled past the defensive line, and carried it alllll the way to the box to plant the ball firmly in the back of the net with a swarm of Porkies around her.  Good one Tess!
</p>
<p>
Half-way though the second 45, another swarm of QPs flocks to the opponent's net to shoot, when a defender sprints back to kick the ball out of Lyndsey's possession.  It's called as a corner!  The ref blows his whistle. No, it's a goal kick!  The ref consults his assistant ref.  No, it's a kick for the other team!  The ref places the ball in the box and backs up.  So.... it's a PK for our team!  Okay?  Okay.  We'll take it then!  Lyndsey knocks the ball to the right corner, the keeper juuuuust gets a hand on it... and Lyndsey follows it up to score goal 5!!
</p>
<p>
Finally, Emma G finished the game the same way it began, with a deadly run through the middle, a shot from the 18, and goooooalll!
</p>
<p>
We're done, yay!
</p>
<p>
Wait no we aren't!  In the final 5 minutes, the ladies in Yellow gain possession in our corner cross the ball to our goal face...bobbles in the defence to fly through the air... smack an opponent in the face... and ricochet into our goal.  Shucks!!!!!  End score: 6-1 to Queens Park.
</p>
<p>
Well it's a wake-up call to the Porkies, a great result but not our finest display! But its a kick in the rump we need so that we can come back stronger, wanting the win more than ever, so beware the Mighty Porkies!!!
</p>
<p>
The game ended on a high-note though, as we celebrated the prolific Lady V's birthday with a scrumptious chocolate cake baked by our other team mummy, Jen.  Thanks Jen, Happy happy Birthday to Lady V, and HAPPY HAPPY (early) Mother's Day to you both!
</p>
<p>
Stay tuned for next week when we (I am serious this time) battle Dunbar at their home field of NCIE.  It's going to be epic!
</p>
