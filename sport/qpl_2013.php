	</p>
	<p>
Not ten minutes later Ronnie sent a cracker across the mouth of the goal straight to Lyndsays epic cranium, to earn us Goal 3!!
	</p>
	<p>
The last goal of the match was earned by none other than Jen M, her 2nd goal of the day - another case of perfect positioning and a great cross.  
	</p>
	<p>
And so in the end we came away with a 4-0 victory and a mild case of dehydration!!!!
	</p>
	<p>
Well done to all and good vibes to the suffering calves of Tess, Dougal, and Vicky.  
	</p>
	<p>
See you next week for a BIG game at NCIE vrs nemesis Dunbar!!!!
	</p>
