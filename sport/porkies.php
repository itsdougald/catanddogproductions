	<h2>The Porkies</h2>
        
	<p>The Cat joined Queens Park football club in March 2006. She was keen to continue her
	footballing skills in Australia and Queens Park Senior Womens fitted the bill. QP has 8 men's teams, 3 senior ladies and two over 35. Cat joined Queens 
	Park Lions (Porkies) in March 2007, which was the older group, hee hee, and rivalled Queens Park Rangers, who were the "younger team" on 
	and off for four seasons. 2012, this has changed. The Porkies are now getting a little younger but still have a good balance of experience over youth. 
	QPR folded 2011 and players played in Div 3. 2012 saw a return to QPR and are in Div 2.</p>
	
	<p>Andy Disley coached for three seasons and handed the reigns over to The Dog in Nov 2006. Dog's first season, in April 2006, was
	a tough start to managing in his first year. They were wrongfully promoted to AAW2, but gained vital experience in all areas. Getting relegated in 2007/2008
	Dog's second season in charge, saw the Porkies AAW3 League and Grand Final Champions over arch rivals Glebe.</p> 
	
	<p>Moving up a league to AAW2 in 2009, meant the Porkies could attack with more experience and confidence. Was a great callenge for the The Dog and back 
	where we belong! Finishing second to undefeated Maroubra, who'd won the league and cup many years in succession, reached the Grand Final losing out to Maroubra -
	was another outstanding season.</p>
	
	<p>2010, we reached the summit, AAW1. With 8 new players we revelled in playing top flight football! Finishing third place was a great achievement, 
	just being pipped by Dunbar Rovers, Porkies managed to beat Maroubra for the first time - Ok, they had 10 players, but who cares!. With a bitter loss to 
	WOB in the semi-finals, our season ended abruptly, but a great crack in our new league. </p>
    
    <p>2011 season was breathtaking in skills and ability. Porkies only lost one game in the league, finishing unofficially top. ESFA deceided to take results after round 2?!
	Unfortunately losing woefully in the semi's to Dunbar luckily had a second chance to reach the finals. Beating Waverley 1 - 0, under a lot of pressure, sadly lost 2 nil to 
	Dunbar in the grand final; were the Porkies ever going to win silverware since those glorious days back in 2006?</p>
	
	<p>2012, we had two ex-professionals bolster our squad, with increadible skill and passion, taking our team to another level. A top Swede sadly had to 
	leave the country and being riddled with injuries, should have been a year of silverware. Finishing a close 3 points behind Dunbar in the league (beat them three out of four game) 
	and losing out to Coogee United in the Grand Final, meant the Porkies came away with nothing but dented egos. Finally 2013 saw the Porkies win the AAW1 
	Grand Final. Wow, now that sounds fantastic! Also entering the State Cup, lost in the Semi-finals and finishing 1 point behind Dunbar, we had our best 
	season on the pith yet!!</p>

	<p>With Dog's 7 season of coaching, could 2014 be his last year and could the Porkies win the illusive AAW1 league?!!!</p>
	
