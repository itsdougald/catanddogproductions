	<p><strong>End of Season Summary Report, written by Coach Dougal [27th Sep]</strong>  <br/>
	With Queens Park Lions season coming to a close and my first year as coach/manager has certainly been a tough year for us all. Being pushed up one 
	league was initially accepted until we played our first game! QPL found it hugely physical and the standard incredibly higher - it meant I had to get
	the girls skills up to an acceptable level. Frustratingly, with erratic turnouts for training, it was proving difficult to get the team up to speed but there were other factors to 
	hurdle too. A 4 week mid-season holiday (to get married!) and 5 weeks of rain since the day we returned from the UK as Mr &amp; Mrs Guppy! With heavy initial defeats, proved an emotional 
	strain for us all...but we hit a turning point when we lost to <a href="/sport/football/qpl_match_reports.cfm?#match-10">Dunbar Rover 2-0</a> on July 8th...
    </p>
    <p>	
	Although we lost, prior to this game we'd also beaten UNSW 3-0. Painstakingly, we'd lost to Dunbar Rovers 5-0 and 5-1 earlier on...but today we played football for the first time of the season. With passing coming into our game
	we'd grown in a little confidence and gave them a real run for their money! Training was perhaps finally paying off. The proceeding matches in the saeson just got better and better.
	The following week we played with only 10 players but we halved the defeat and even managed to score! Coming so close to a point against 
	<a href="/sport/football/qpl_match_reports.cfm?#match-14">Pagewood</a> on August 5th gave us so much confidence. The spirits were high. With Season nearing an end we knew our final 5 matches
	would be a close and ready to do battle. Unfortunately we had to play QPR two days in a row. Saturday's game was a just deserved draw, perhaps we could have won but really missed Sonja's 
	extra flavour to the mix, as it also showed on <a href="/sport/football/qpl_match_reports.cfm?#match-16-17">Sunday</a>. Frustratingly going down 1-0 early on in the game we couldn't convert our many chances. 
	With the heavens opening mid-way through the second half made life even harder. Sonja braved the last 15 minutes of the game and made a huge difference. Unfortunately nothing came off and bitterly lost with
	a soft goal. We were numb but a few of us went to the pub and talked about the season (for some) for over 5 hours...hic hic!</p>
	<p>
	With our open wounds rehealing and a great training session, two of our strongest games followed. Our final game against <a href="qpl_match_reports.cfm?#match-18">Dunbar Rovers</a> and our very final game against 
	<a href="/sport/football/qpl_match_reports.cfm?#match-20">Pagewood</a>. With a painfull free-kick giveaway at the start of the Dunabr game meant our tails were in between our legs (once again) but only briefly.
	We grew in huge confidence and was end to end. So many hard runs from Chappers and Skelton, in her new found position, had some excellent
	opportunities....but to no avail. We played for 90 minutes which again was another turnaround to our game, something which I'd never seen the girls do. Fantastic game to 
	watch. 
	</p>
	<p>Pagewood, our final game, was time to get revenge since the poor referring decision depriving us of a deserved point; he was barely old enough to change his nappies let alone make
	a decision in front of 22 feisty females!! Anyway bac to our final game, the start was oh so familiar. Another cheap goal put us down 1-0 in the first 5 minutes. Oh so common girls but we looked tired and so un-inventive until...
	the last 20 minutes of the first half. Suddenly the game had turned around. Excitingly we were breaking away quickly and finally getting crosses were firing in with through balls onto our forwards. Once again we grew in confidence.
	Second half was the most domineering game of footabll I'd seen from the girls all season. Phenominal. So many chances. So many close misses. Because most of the season we weren't used to being on the 
	attack we weren't quite making the final touches count. With a few crosses in, no-one was there, but there was a chance where Marga had whipped a cross in from the edge of
	the left side and Kerrie got a head to it, sailing just wide of the right hand post. Incredible.
	</p>
	<p>When the final whistle blew we had finished on an absolute high, there was really not much else the girls could do but hold their heads high; what a grand finale. My thoughts were 
	if only we were now halfway through the season I think we could have contended for a top four play-off spot but not sure if the girls would agree...
	</p>
	<p>
	Roll on next season!
	</p>
	
